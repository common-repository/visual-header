if ( !function_exists ( "vh_library" )){
add_action('wp_ajax_vh_library', 'vh_library');
add_action('wp_ajax_nopriv_vh_library', 'vh_library'); 
function vh_library(){
	
  	$library= vh_library_array();
	$count=0;
 
	if(!empty($library)){
		foreach($library as $id=> $value):
		$count++;
		$selected = $count==1?'selected':'';
		$img =!empty($value['img'])?$value['img']:'';
		
		echo '<li class="vh_model_item '.esc_attr($selected).'" data-id="'.esc_attr($id).'"  >';
		echo '<div class="vh_model_image"  >';
			echo '<img src="'.esc_url($img).'" />';
		echo '</div>';
 			
		if(!empty($value['name'])){ 
			echo '<span>'.esc_html($value['name']).'</span>';
		}
										
		echo '</li>';
			endforeach;
		}
        
 	die();
}
} 

if ( !function_exists ( "vh_library_array" )){
function vh_library_array() { 
 
	global $vh_library;
 	$id=array();
	if(has_filter('vh_library')) {
		$vh_library = apply_filters('vh_library', $id);
	}
 	return 	$vh_library;		 
 
}
}
 